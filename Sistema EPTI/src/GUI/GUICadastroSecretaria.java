    /**
     * @author Maikel Maciel Rönnau
     * @version 1.0
     * @since 06/01/2015
     */
    private void cadastrar(){
        
        try {
            
            //Recebendo dados da interface:
            Validacao.validarNomeProprio(jtNome.getText());
            Validacao.validarSigla(jtSigla.getText());
            //Fim da entrada de dados.
            
            //Instanciando objeto da classe:
            SecretariaVO sec = new SecretariaVO();
            
            //Atribuindo dados ao objeto:            
            sec.setNomeSecretaria(Tratamento.converterInicialDeCadaPalavraMaiucula(jtNome.getText()));
            sec.setSiglaSecretaria(jtSigla.getText().toUpperCase());
            
            //Instanciando objeto para acessar o método de insersão:
            SecretariaServicos ss = ServicosFactory.getSecretariaServicos();
            
            //Cadastrando secretaria:
            ss.cadastrarSecretaria(sec);
            
            //Mensagem de confirmação:
            JOptionPane.showMessageDialog(
                this,
                "Secretaria cadastrada com sucesso!",
                "Cadastro concluído",
                JOptionPane.INFORMATION_MESSAGE);
            //Fim da mensagem de confirmação.
            
            //Fechando janela:
            cancelar();
        } catch (SQLException e) {
            
            //Mensagem de erro:
            JOptionPane.showMessageDialog(
                this,
                "Falha ao cadastrar secretaria! "
              + "\n\nNome ou sigla já cadastrados.",
                "Erro",
                JOptionPane.WARNING_MESSAGE);
            //Fim da mensagem de erro.
        } catch (Exception e){
            
            //Mensagem de erro:
            JOptionPane.showMessageDialog(
                this,
                e.getMessage(),
                "Erro",
                JOptionPane.WARNING_MESSAGE);
            //Fim da mensagem de erro.
        }//Fecha catch.        
    }//Fecha método cadastrar.
    
    /**
     * @author Maikel Maciel Rönnau
     * @version 1.0
     * @since 06/01/2015
     */
    private void cancelar(){
        
        //Fechando a janela:
        dispose();
    }//Fecha método cancelar.
    
